<p>25)<strong>What is the difference between embassy and consulate?</strong></p>



<p>A permanent diplomatic mission of one country in another is typically known as an embassy, and is usually located in the host country’s capital. The person in charge of the mission is known as an ambassador. The term “embassy” is often used to refer to the building or compound housing an ambassador’s offices and staff. Technically, however, “embassy” refers to the diplomatic delegation itself, while the office building in which they work is known as a chancery.
The office of a Consul is termed a Consulate, and is usually subordinate to the state’s main representation in that foreign country, usually an Embassy or High Commission between Commonwealth countries, in the capital city of the host state. Like the term embassy, the word consulate may refer not only to the office of consul, but also to the building occupied by the consul and his or her staff. In capital cities, the consulate may share the premises with the embassy itself.</p>

