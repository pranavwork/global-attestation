           <p><H1><strong>Legal Translation</strong></H1></p>

 <p style="text-align: justify;">Legal translation is the translation of texts within the field of law. As the law is a culture-dependent subject field, <strong>legal translation </strong>is not necessarily linguistically transparent. In-transparency in translation can be avoided somewhat by the use of Latin legal terminology, where possible.</p>
<p style="text-align: justify;"> The term legal translation refers to the translation of any text used within the legal system. As documents used for legal purposes are generally required to be submitted in the official language of a relevant jurisdiction the term can encompass a wide variety of texts including, but not limited to,<strong> witness statements, legal rulings and precedents, filed patents, transcripts, official reports, financial documents and identity documentation</strong>. A wide variety of other sources of information can also be subject to legal translation depending on their contextualized relationship to legal proceedings.
 <p style="text-align: justify;">For example, the Will and testament of an expatriate may be subject to translation into the language of the jurisdiction in which they have died in order for a probate lawyer to begin the process of executing the instructions contained therein. Generally, legal translation services are only undertaken by those with specialist knowledge as mistranslations, especially of contracts, can carry significant financial and legal consequences.</p>

