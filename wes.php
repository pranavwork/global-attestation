           <p><H1><strong>WES - World Education Services</strong></H1></p>

 <p style="text-align: justify;"><strong>World Education Services </strong>abbreviated as WES, it is a legal recognition of <a href="http://globalattestation.com/educational-certificate-attestation.php"><strong>Educational Certificates</strong></a>. WES an International non-profit organization emerged in 1974, for enriching  educational qualification of the immigrants. It is prevalent in the <strong>United States (US)</strong> and <strong>Canada</strong> and help the immigrants to easily relocate or shift to these countries. WES officials grant its accreditation to different types of educational documents like <strong>degree documents</strong>, <strong>diploma certificates</strong>,<strong> HSC certificates</strong>, <strong>SSLC certificates</strong>. It provides a vast variety of opportunities for talented persons. It converts your document legally approved and acts as a bridge between you and your destination. </p>

<p style="text-align: justify;">WES Verification is doing for getting a <strong>permanent Residential visa</strong> for the foreign countries. This Verification is done in degree final certificate copy, final mark sheet or in transcript certificate. Through World Education Services accreditation you can fully utilize your educational qualification. Migration purposes, job purposes, higher education purposes, etc...</p>

<p><H2><strong>How to Apply for WES online ? </strong></H2></p>
<p style="text-align: justify;">1. Create an account of the WES: <strong>https://www.wes.org/apply/createacco…?loc=onlineapp</strong></p>

<p style="text-align: justify;">2. Log in to the account. Now you will see tabs: Personal info, Your Education, Your Evaluation etc…</p>

<p style="text-align: justify;">3. On the Education tab, Enters the university details and click on the <strong>“I have reviewed the required documents for this               credential, and agree to submit my documents as required by WES.”</strong></p>

<p style="text-align: justify;">4. On the next page select the type of evaluation you want. You may need to select the course by course evaluation.</p>

<p style="text-align: justify;">5. On the same page, Turn around time will be selected by the system automatically. Sometimes 3-business day and same day     services will not be available.</p>

<p style="text-align: justify;">6. After that add the institutional name. While searching the institution name select <strong>“Educational Institution.”</strong></p>

<p style="text-align: justify;">7. You can add as many institutions you want.</p>

 <p><H3><strong>Documents required for WES Evaluation :</strong></H3></p>

